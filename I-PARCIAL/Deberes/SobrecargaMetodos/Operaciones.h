template <typename T>
class Operaciones {

	public:
		//suma
		T sumar(T,T);
		T sumar(T,T,T);
		T sumar (T,T,T,T);
		T sumar (T,T,T,T,T);
		//resta
		T restar(T);
		T restar(T,T);
		T restar (T,T,T);
		T restar (T,T,T,T,T);
		T restar (T,T,T,T,T,T);
		
		//multiplicacio
		T multiplicar (T);
		T multiplicar(T,T);
		T multiplicar (T,T,T);
		T multiplicar(T,T,T,T,T,T);	
		
		
			
		
		//Calcular Mayor
		T calcularMayor(T,T);
		T calcularMayor(T,T,T);
		T calcularMayor(T,T,T,T,T);
		T calcularMayor(T, T,T, T);
	
};
